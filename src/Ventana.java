    public void consulta(String where) {
        try {
            DriverManager.registerDriver(new com.mysql.cj.jdbc.Driver());
            Connection conexion
                    = DriverManager.getConnection("jdbc:mysql://localhost:3308/TiendaDB?serverTimezone=UTC", "Admin", "edimir123.");
            Statement s = conexion.createStatement();
            String SQL = "SELECT * FROM `tienda`.`productos`";
            if (!where.isEmpty()) {
                SQL += where;
            }
            ResultSet resultadoConsulta = s.executeQuery(SQL);
            if (m.getRowCount() > 0) {
                int total = m.getRowCount();
                for (int i = 1; i <= total; i++) {
                    m.removeRow(0);
                }
            }
            while (resultadoConsulta.next()) {
                String[] data = new String[4];
                data[0] = "" + resultadoConsulta.getInt(1);
                data[1] = "" + resultadoConsulta.getString(2);
                data[2] = "" + resultadoConsulta.getString(3);
                data[3] = "" + resultadoConsulta.getString(4);
                m.addRow(data);
            }
            conexion.close();
        } catch (Exception e) {
            mensaje(e.getMessage());
        }
    }

