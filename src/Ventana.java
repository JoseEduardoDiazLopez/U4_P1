    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        try {
            DriverManager.registerDriver(new com.mysql.cj.jdbc.Driver());
            Connection conexion = DriverManager.getConnection("jdbc:mysql://localhost:3308/TIENDADB?serverTimezone=UTC", "Admin", "edimir123.");
            Statement s = conexion.createStatement();
            String SQL = "INSERT INTO `tienda`.`productos` (`idProductos`,`DESCRIPCION`,`PRECIO`,`EXISTENCIA`) "
                    + "VALUES (NULL, '%des', '%pre', '%stock');";
            SQL = SQL.replace("%des", txtDes.getText());
            SQL = SQL.replace("%pre", txtP.getText());
            SQL = SQL.replace("%stock", txtEx.getText());
            int respuesta = s.executeUpdate(SQL);
            String texto = "";
            if (respuesta != 0) {
                texto = "SE INSERTO CORRECTAMENTE";
            } else {
                texto = "Error no se pudo insertar";
            }
            mensaje(texto);
            limpiar();
            conexion.close();
            consulta("");
        } catch (Exception e) {
            mensaje(e.getMessage());
        }
    }//GEN-LAST:event_btnInsertarActionPerformed
    private void eliminar(String id) {
        try {
            DriverManager.registerDriver(new com.mysql.cj.jdbc.Driver());
            Connection conexion = DriverManager.getConnection("jdbc:mysql://localhost:3308/TIENDADB?serverTimezone=UTC", "Admin", "edimir123.");
            Statement s = conexion.createStatement();
            String SQL = "DELETE FROM `tienda`.`productos` WHERE idProductos =" + id;
            int resultado = s.executeUpdate(SQL);
            if (resultado == 0) {
                mensaje("ERROR " + id);
            } else {
                mensaje("Eliminado " + id);
            }
            conexion.close();
        } catch (Exception e) {
            mensaje(e.getMessage());
        }
        consulta("");
    }

